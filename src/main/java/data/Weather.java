package data;

public class Weather{

    String observation_time;
    int temperature;
    int feelslike;
    int visibility;
    int wind_speed;

    public String getObservationTime() {
        return observation_time;
    }

    public int getTemperature() {
        return temperature;
    }

    public int getFeelsLike() {
        return feelslike;
    }

    public int getVisibility() {
        return visibility;
    }

    public int getWindSpeed() {
        return wind_speed;
    }
}
